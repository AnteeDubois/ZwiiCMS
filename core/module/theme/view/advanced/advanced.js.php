/**
 * Aperçu en direct
 */
$("#themeAdvancedCss").on("change keydown keyup", function() {
	// Ajout du css au DOM
	$("#themePreview").remove();
	$("<style>")
		.attr("type", "text/css")
		.attr("id", "themePreview")
		.text($(this).val())
		.appendTo("head");
});